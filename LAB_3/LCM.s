	AREA RESET, DATA, READONLY
	EXPORT __Vectors
	
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0, =NUM1
	LDR R1, =NUM2
	LDR R0, [R0]
	LDR R1, [R1]
UP	MOV R4, #0
	BL DIVIDE
	CMP R4, #0
	BEQ DOWN
	ADD R0, R0
	B UP
DOWN
	LDR R2, =LCM
	STR R0, [R2]
STOP B STOP

DIVIDE
	MOV R4, R0
UP1	CMP R4, #0
	BLE EXIT
	SUB R4, R1
	B UP1
EXIT BX LR

NUM1 DCD 0XC
NUM2 DCD 0X08

	AREA mydata, DATA, READWRITE
LCM DCD 0

	END